DROP TABLE IF EXISTS ADDRESS_BOOK;
CREATE TABLE ADDRESS_BOOK(
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  customerId BIGINT,
  default_address SMALLINT,
  name VARCHAR(255),
  street VARCHAR(255),
  city VARCHAR(150),
  state VARCHAR(25),
  zipcode VARCHAR(10),
  phoneNumber VARCHAR(25),
  PRIMARY KEY (id));